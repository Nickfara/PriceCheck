# noinspection PyBroadException
def send(call, answer:str, btns:tuple, row_width:int=3):
    """
        –û—Ç–ø—Ä–∞–≤–∫–∞ –∏–ª–∏ —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è

        :param row_width: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–Ω–æ–ø–æ–∫ –≤ —Å—Ç—Ä–æ–∫–µ.
        :param call: –î–∞–Ω–Ω—ã–µ –æ –∫–æ–º–∞–Ω–¥–µ –∏–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–∏.
        :param answer: –¢–µ–∫—Å—Ç –¥–ª—è —Å–æ–æ–±—â–µ–Ω–∏—è.
        :param btns: –ö–æ—Ä—Ç–µ–∂ —Å –∫–Ω–æ–ø–∫–∞–º–∏(('–¢–µ–∫—Å—Ç', '–ö–æ–º–∞–Ω–¥–∞'), ('–¢–µ–∫—Å—Ç', '–ö–æ–º–∞–Ω–¥–∞')).
        """

    global new_message

    
    uid = call.from_user.id
    mid = call.message.message_id

    btns_markup = []
    
    for lot in btns: # –†–∞—Å–ø–∞–∫–æ–≤–∫–∞ –∫–æ—Ä—Ç–µ–∂–∞ –∏ —Å–æ–∑–¥–∞–Ω–∏–µ —Å–ø–∏—Å–∫–∞ –≥–æ—Ç–æ–≤—ã—Ö –∫–Ω–æ–ø–æ–∫
        btns_markup.append(types.InlineKeyboardButton(text=lot[0], callback_data=lot[1]))

    markup = types.InlineKeyboardMarkup(row_width=row_width) # –°–æ–∑–¥–∞–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –∫–Ω–æ–ø–æ–∫
    markup.add(*btns_markup) # –ù–∞–ø–æ–ª–Ω–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –∫–Ω–æ–ø–∫–∞–º–∏

    for r in range(-4, 4):  # –ß–∏—Å—Ç–∏—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –≤–æ–∫—Ä—É–≥ —Ç–µ–∫—É—â–µ–≥–æ
        try:
            bot.delete_message(chat_id=uid, message_id=(mid + r))
        except:
            pass

    if new_message == 0:  # –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è.
        try:

            bot.edit_message_text(chat_id=uid, message_id=mid, text=answer, reply_markup=markup,
                                  parse_mode='MarkdownV2')
        except:
            new_message = 1  # –ü—Ä–∏ —Å—Ä–∞–±–∞—Ç—ã–≤–∞–Ω–∏–∏ –∏—Å–∫–ª—é—á–µ–Ω–∏—è –±–ª–æ–∫–∏—Ä—É–µ—Ç—Å—è –∏–∑–º–µ–Ω–µ–Ω–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è.

    if new_message == 1:  # –û—Ç–ø—Ä–∞–≤–∫–∞ –Ω–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è.
        bot.send_message(chat_id=uid, text=answer, reply_markup=markup, parse_mode='MarkdownV2')


